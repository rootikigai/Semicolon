// Function that takes an array of numbers and returns an array of the square of the elements in ascending order
public class ArraySort{
  public static int squareArraySort(int[] arrays){
    int sqr = arrays[0];
    int[] numbers = {};
    for(int i = 0; i < arrays.length; i++){
      sqr = Math.pow(numbers[i], 2);
    }
  }
}
